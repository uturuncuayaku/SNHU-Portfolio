bool run_query(sqlite3* db, const std::string& sql, std::vector< user_record >& records)
{

  // clear any prior results
  records.clear();

  // If the query doesn't have a weird 'or' condition
  // that evaluates to true, we have parsed it correctly
  // otherwise we don't let the query run on the database
  if(parseQuery(sql)) {
    std::cout << " Please review sql query for possible injection!" << std::endl;
    return false;
  }

  char* error_message;
  if(sqlite3_exec(db, sql.c_str(), callback, &records, &error_message) != SQLITE_OK)
  {
    std::cout << "Data failed to be queried from USERS table. ERROR = " << error_message << std::endl;
    sqlite3_free(error_message);
    return false;
  }

  return true;
}

